                <p>Also, the role of a developer is set to grow in importance by 29% to businesses over the next three years, and with these two skills along with keeping up to date with current technologies in the industry, chances of employment over the coming months seem more positive (Manchester Digital Skills Audit, 2019). </p>
            
                <p>Ross (2019) state that in 2019, there was over £4.9 billion digital tech turnover in the North West as well as 88,000 currently employed. This highlights that digital businesses are a key figure in the North West, contributing massively to the economy, as well as a large source of employment for people like me soon to be seeking employment within the digital industry.</p>
            </div>

            <div class="blog-body blog-container">
                <h3 class="blog-body__titles">My thoughts on the role of a front end developer</h3>

                <p>I’m excited, eager and also a little nervous to potentially working in the digital industry in six-months’ time and have already been looking at various front-end developer role requirements. As in the picture below, many role requirements gave a large list of skills and knowledge required – making me feel as though I’m not qualified enough. This makes me feel as though I need to learn everything, and as Berner (2016) indicates, learning everything at once will be bad because you can’t retain all the information.</p>
            </div>

