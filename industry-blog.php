                    <p>The chaos what we call Brexit is causing tension and uncertainty for many businesses over the UK – including the digital industry – and I looked to the <a href = "https://www.manchesterdigital.com/post/manchester-digital/digital-skills-audit-2019" class = "blog__links" title = "Manchester Skills Audit Report 2019">Manchester Digital Skills Audit</a> and <a href = "https://technation.io/report2019/" class = "blog__links" title = "Tech Nation Report 2019">Tech Nation </a> reports to investigate how this chaos may impact my prospects. Manchester Digital Skills Audit (2019) pointed out in their report that over the last two years, business growth by turnover has declined and in the last twelve months only 54% of business reported growth.</p>
            
                <p>Ross (2019) stated in a report surveyed by <a href = "https://www.techuk.org/insights/news/item/16028-techuk-members-survey-how-prepared-are-members-for-a-no-deal-brexit" class = "blog__links" title = "Tech UK Brexit report">Tech UK</a> that 71% of their members said that a no deal Brexit would have a negative impact on their business. It also indicates that there was an 11% decrease in staffing levels, and all this has some direct correlation to the uncertainty surrounding Brexit. In addition, 34% of businesses reported a negative impact on their business with issues such as clients being cautious with investments. These statistics alone can be daunting to someone who will be seeking employment in the digital industry in the next year and how my opportunity for employment can be at risk if the UK come out with a bad deal or no deal.</p>
                </div>


            <div class="blog-body blog-container">
                <h3 class="blog-body__titles">Some positive thoughts</h3>
            
                <p>On the contrary, Manchester Digital Skills Audit (2019) report that 53% of digital business in Greater Manchester recruit directly from Manchester Metropolitan University. The report states HTML and JavaScript are two of the five most in demand programming languages, both being a strong point of mine and languages being educated on for my web development degree</p>
            </div>

            <div class="blog-body blog-container">
